/**
 * Предустановленные виды рыб и дичи.
 */
object Species {

    /**
     * Виды рыб по категориям.
     */
    val fishSpecies = listOf(
        // Хищные
        "Щука", "Окунь", "Судак", "Сом", "Жерех", "Голавль", "Язь",
        // Мирные
        "Карп", "Карась", "Лещ", "Плотва", "Линь", "Густера", "Белый амур",
        // Лососевые
        "Форель", "Хариус", "Таймень", "Сиг", "Нельма",
        // Другие
        "Налим", "Угорь", "Сазан", "Толстолобик"
    )

    /**
     * Виды дичи по категориям.
     */
    val gameSpecies = listOf(
        // Копытные
        "Лось", "Кабан", "Косуля", "Олень", "Марал",
        // Пернатая дичь
        "Утка", "Гусь", "Фазан", "Вальдшнеп", "Тетерев", "Глухарь", "Рябчик", "Куропатка", "Перепел",
        // Пушная дичь
        "Заяц", "Лиса", "Бобр", "Енот", "Барсук", "Ондатра"
    )

    /**
     * Получить список видов по типу активности.
     */
    fun getSpeciesByActivityType(activityType: ActivityType): List<String> {
        return when (activityType) {
            ActivityType.FISHING -> fishSpecies
            ActivityType.HUNTING -> gameSpecies
        }
    }
}
